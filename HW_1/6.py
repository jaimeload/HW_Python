n = int(input('Введите шестизначный номер билета: '))
n123 = n // 1000
n456 = n % 1000
sum123 = 0
sum456 = 0
while n123 > 0:
    sum123 += n123 % 10
    n123 //= 10
while n456 > 0:
    sum456 += n456 % 10
    n456 //= 10
if sum123 == sum456:
    print('Билет счастливый')
else:
    print('Билет обычный')