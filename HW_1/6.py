n = int(input('Введите шестизначный номер билета: '))
sum123 = 0
sum456 = 0
n123 = n // 1000
n456 = n - (n123 * 1000)
while n123 > 0:
     x = n123 % 10
     sum123 += x
     n123 //= 10
while n456 > 0:
    x = n456 % 10
    sum456 += x
    n456 //= 10
if sum123 == sum456:
    print('Билет счастливый')
else:
    print('Билет обычный')